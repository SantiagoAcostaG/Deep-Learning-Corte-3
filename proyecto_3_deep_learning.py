# ============================================================
# SECCIÓN 4 — MODELO AVANZADO: DistilBERT (Transformers)
# ============================================================
from transformers import DistilBertTokenizerFast, DistilBertForSequenceClassification
from sklearn.metrics import classification_report, confusion_matrix, ConfusionMatrixDisplay

# Descargamos el modelo base de HuggingFace
tokenizer = DistilBertTokenizerFast.from_pretrained('distilbert-base-uncased')
modelo_bert = DistilBertForSequenceClassification.from_pretrained('distilbert-base-uncased', num_labels=2).to(device)

# Dataset especial para BERT (Él hace su propia tokenización)
class DatasetBERT(Dataset):
    def __init__(self, textos, etiquetas):
        self.encodings = tokenizer(list(textos), truncation=True, padding=True, max_length=128, return_tensors='pt')
        self.labels = torch.tensor(etiquetas, dtype=torch.long)

    def __len__(self):
        return len(self.labels)

    def __getitem__(self, idx):
        item = {key: val[idx] for key, val in self.encodings.items()}
        item['labels'] = self.labels[idx]
        return item

train_loader_bert = DataLoader(DatasetBERT(X_train, y_train), batch_size=16, shuffle=True)
val_loader_bert   = DataLoader(DatasetBERT(X_val, y_val), batch_size=16, shuffle=False)
test_loader_bert  = DataLoader(DatasetBERT(X_test, y_test), batch_size=16, shuffle=False)

optimizador_bert = torch.optim.AdamW(modelo_bert.parameters(), lr=2e-5)

import time

# Bucle de Entrenamiento BERT (Restaurado a 5 épocas)
EPOCHS_BERT = 5
print("\nIniciando Fine-Tuning de DistilBERT...")
start_time = time.time()

for epoch in range(EPOCHS_BERT):
    # ENTRENAMIENTO
    modelo_bert.train()
    train_loss = 0.0
    for batch in train_loader_bert:
        input_ids = batch['input_ids'].to(device)
        attention_mask = batch['attention_mask'].to(device)
        labels = batch['labels'].to(device)

        optimizador_bert.zero_grad()
        outputs = modelo_bert(input_ids, attention_mask=attention_mask, labels=labels)

        loss = outputs.loss
        loss.backward()
        optimizador_bert.step()
        train_loss += loss.item()

    # VALIDACIÓN
    modelo_bert.eval()
    val_loss = 0.0
    with torch.no_grad():
        for batch in val_loader_bert:
            input_ids = batch['input_ids'].to(device)
            attention_mask = batch['attention_mask'].to(device)
            labels = batch['labels'].to(device)

            outputs = modelo_bert(input_ids, attention_mask=attention_mask, labels=labels)
            val_loss += outputs.loss.item()

    print(f"Época {epoch+1:02d}/{EPOCHS_BERT} | Train Loss: {train_loss/len(train_loader_bert):.4f} | Val Loss: {val_loss/len(val_loader_bert):.4f}")

tiempo_total_bert = time.time() - start_time
print(f"\n[*] Fine-Tuning DistilBERT completado en {tiempo_total_bert//60:.0f}m {tiempo_total_bert%60:.0f}s")

# Evaluación Final en el Test Set
modelo_bert.eval()
predicciones_finales = []
etiquetas_reales = []

with torch.no_grad():
    for batch in test_loader_bert:
        input_ids = batch['input_ids'].to(device)
        attention_mask = batch['attention_mask'].to(device)
        labels = batch['labels']

        outputs = modelo_bert(input_ids, attention_mask=attention_mask)
        _, preds = torch.max(outputs.logits, 1)

        predicciones_finales.extend(preds.cpu().numpy())
        etiquetas_reales.extend(labels.numpy())

print("\n" + "="*50)
print("REPORTE DE DISTILBERT EN TEST SET")
print("="*50)
print(classification_report(etiquetas_reales, predicciones_finales, target_names=['Ham', 'Spam']))

# Dibujar matriz
ConfusionMatrixDisplay.from_predictions(etiquetas_reales, predicciones_finales, display_labels=['Ham', 'Spam'], cmap='Blues')
plt.title("Matriz de Confusión - DistilBERT")
plt.show()

