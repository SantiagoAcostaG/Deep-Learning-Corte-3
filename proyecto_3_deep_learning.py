def analizar_mensaje_xai(mensaje):
    if not mensaje.strip():
        return "Escribe un mensaje por favor.", ""

    probabilidades = predecir_probabilidades([mensaje])[0]
    es_spam = probabilidades[1] > 0.5

    # LIME saca las palabras más importantes
    explicacion = explicador.explain_instance(mensaje, predecir_probabilidades, num_features=5, labels=[1])
    palabras_top = [word for word, weight in explicacion.as_list(label=1) if weight > 0]

    # Armar la respuesta reactiva
    if es_spam:
        estado = f"🚨 ALERTA: SPAM DETECTADO (Confianza: {probabilidades[1]*100:.1f}%)"

