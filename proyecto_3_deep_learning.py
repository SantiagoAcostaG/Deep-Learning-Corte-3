# ============================================================
# SECCIÓN 3 — BASELINE: BiLSTM con Word Embeddings
# ============================================================
from torch.utils.data import Dataset, DataLoader
from torch.nn.utils.rnn import pad_sequence
import torch.nn as nn

# 1. Creamos un diccionario manual de palabras (estilo estudiante)
word2idx = {'<PAD>': 0, '<UNK>': 1}
idx2word = {0: '<PAD>', 1: '<UNK>'}
idx = 2

for oracion in X_train:
    for palabra in oracion.split():
        if palabra not in word2idx:
            word2idx[palabra] = idx
            idx2word[idx] = palabra
            idx += 1

print(f"Palabras únicas en vocabulario: {len(word2idx)}")

# Función para convertir oraciones a números
def codificar_texto(texto, max_len=128):
    tokens = [word2idx.get(w, 1) for w in texto.split()] # 1 es <UNK> si no la conoce
    return tokens[:max_len]

# 2. Dataset de PyTorch
class SMSDataset(Dataset):
    def __init__(self, texts, labels):
        self.data = []
        for text, label in zip(texts, labels):
            tokens = codificar_texto(text)
            self.data.append((torch.tensor(tokens, dtype=torch.long),
                              torch.tensor(label, dtype=torch.long)))

    def __len__(self):
        return len(self.data)

    def __getitem__(self, index):
        return self.data[index]

def pad_collate(batch):
    texts, labels = zip(*batch)
    texts_padded = pad_sequence(texts, batch_first=True, padding_value=0)
    return texts_padded, torch.stack(labels)

train_loader = DataLoader(SMSDataset(X_train, y_train), batch_size=32, shuffle=True, collate_fn=pad_collate)
val_loader   = DataLoader(SMSDataset(X_val, y_val), batch_size=32, shuffle=False, collate_fn=pad_collate)
test_loader  = DataLoader(SMSDataset(X_test, y_test), batch_size=32, shuffle=False, collate_fn=pad_collate)

# 3. Definición de la Red BiLSTM
class MiBiLSTM(nn.Module):
    def __init__(self, vocab_size, embed_dim=64, hidden_dim=64):
        super(MiBiLSTM, self).__init__()
        self.embedding = nn.Embedding(vocab_size, embed_dim, padding_idx=0)
        # Bidirectional = True es lo que hace que lea de izquierda a derecha y viceversa
        self.lstm = nn.LSTM(embed_dim, hidden_dim, batch_first=True, bidirectional=True)
        self.dropout = nn.Dropout(0.3)
        self.fc = nn.Linear(hidden_dim * 2, 2) # *2 porque es bidireccional (ida y vuelta)

    def forward(self, x):
        emb = self.embedding(x)
        out, (hidden, cell) = self.lstm(emb)
        # Tomamos el último estado oculto de ambas direcciones
        hidden_concatenado = torch.cat((hidden[-2,:,:], hidden[-1,:,:]), dim=1)
        hidden_drop = self.dropout(hidden_concatenado)
        return self.fc(hidden_drop)

modelo_lstm = MiBiLSTM(len(word2idx)).to(device)
criterio = nn.CrossEntropyLoss(weight=pesos_tensor) # Usamos los pesos por el desbalance
optimizador = torch.optim.Adam(modelo_lstm.parameters(), lr=0.001)

import time

# 4. Bucle de Entrenamiento (Restaurado a 10 épocas)
EPOCHS = 10
print("\nIniciando entrenamiento del BiLSTM...")
start_time = time.time()

for epoch in range(EPOCHS):
    # Fase de Entrenamiento
    modelo_lstm.train()
    train_loss = 0.0
    for textos, etiquetas in train_loader:
        textos, etiquetas = textos.to(device), etiquetas.to(device)

        optimizador.zero_grad()
        predicciones = modelo_lstm(textos)
        loss = criterio(predicciones, etiquetas)
        loss.backward()
        optimizador.step()

        train_loss += loss.item()

    # Fase de Validación
    modelo_lstm.eval()
    val_loss = 0.0
    correctos = 0
    total = 0
    with torch.no_grad():
        for textos, etiquetas in val_loader:
            textos, etiquetas = textos.to(device), etiquetas.to(device)
            predicciones = modelo_lstm(textos)
            loss = criterio(predicciones, etiquetas)
            val_loss += loss.item()

            _, clase_predicha = torch.max(predicciones, 1)
            correctos += (clase_predicha == etiquetas).sum().item()
            total += etiquetas.size(0)

    print(f"Época {epoch+1:02d}/{EPOCHS} | Train Loss: {train_loss/len(train_loader):.4f} | Val Loss: {val_loss/len(val_loader):.4f} | Val Acc: {correctos/total:.4f}")

tiempo_total = time.time() - start_time
print(f"\n[*] Entrenamiento BiLSTM completado en {tiempo_total//60:.0f}m {tiempo_total%60:.0f}s")

