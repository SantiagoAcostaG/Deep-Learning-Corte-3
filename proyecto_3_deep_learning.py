import re

def limpiar_texto(texto):
    texto = str(texto).lower() # Todo a minúsculas
    texto = re.sub(r'http\S+|www\S+', 'URL', texto) # Cambiamos links por la palabra URL
    texto = re.sub(r'\d+', 'NUMERO', texto) # Cambiamos números por la palabra NUMERO
    texto = re.sub(r'[^\w\s]', ' ', texto) # Quitamos puntuación rara
    texto = re.sub(r'\s+', ' ', texto).strip() # Quitamos espacios extra
    return texto

