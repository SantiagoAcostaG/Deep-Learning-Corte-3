def predecir_probabilidades(textos):
    textos_limpios = [limpiar_texto(t) for t in textos]

    encodings = tokenizer(textos_limpios, truncation=True, padding=True, max_length=128, return_tensors='pt').to(device)
    with torch.no_grad():
        outputs = modelo_bert(**encodings)
    probabilidades = torch.softmax(outputs.logits, dim=1).cpu().numpy()
    return probabilidades

